/*write a java program to print   
  *   *   *  *  *
   *   *   *  *  *
   *   *   *  *  *
   *   *   *  *  *
   *   *   *  *  *  By using nested for loop*/
class loops1feb10{
  public static void main(String[] args){
    int i,j; 
              
  for(i=1;i<=5;i++)
  {           
     for(j=1;j<=5;j++)
     {
         System.out.print("*");
      }
     System.out.println();
   } 
 
  }
}
 